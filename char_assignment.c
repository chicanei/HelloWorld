//字元常數將字元的ASCII值和它所代表的字元連接起來
//將字元常數指定給字元變數並印出來
#include <stdio.h>
int main(void){
    char c;
    c = 'H';
    printf("%c", c);
    c = 'E';
    printf("%c", c);
    c = 'L';
    printf("%c", c);
    c = 'L';
    printf("%c", c);
    c = 'O';
    printf("%c", c);
    c = '!';
    printf("%c", c);
    return 0;
}
